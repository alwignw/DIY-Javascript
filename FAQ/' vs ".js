//Dalam JavaScript, single quote (') dan double quote (") digunakan untuk menandai string. 
//Pada dasarnya, keduanya berfungsi sama dan dapat digunakan secara bergantian. Namun, ada beberapa perbedaan yang harus diperhatikan:


// Penggunaan tanda kutip di dalam string
// Jika kita ingin menggunakan tanda kutip di dalam string, 
// kita harus memperhatikan tanda kutip yang digunakan untuk menandai string. 
// Jika kita menggunakan single quote untuk menandai string, 
// kita bisa menggunakan double quote di dalam string tanpa masalah, dan sebaliknya. Contohnya:

let nama = "Ali's bookstore"; // double quote di luar, single quote di dalam
let kota = 'I live in "Jakarta"'; // single quote di luar, double quote di dalam


// Konvensi gaya kode
// Terkadang penggunaan single quote atau double quote dapat dipengaruhi oleh konvensi gaya kode yang diterapkan pada proyek tertentu.
// Beberapa konvensi mungkin mendorong penggunaan salah satu jenis tanda kutip untuk konsistensi.

// Escape character
// Dalam JavaScript, backslash (\) digunakan sebagai escape character untuk menampilkan karakter khusus di dalam string.
// Jika kita ingin menampilkan tanda kutip yang sama dengan tanda kutip yang digunakan untuk menandai string, 
// kita bisa menggunakan escape character. Contohnya:

let kata = "I said, \"Hello world!\""; // menggunakan double quote dan escape character
let kalimat = 'I\'m learning JavaScript'; // menggunakan single quote dan escape character


// Secara umum, baik single quote maupun double quote dapat digunakan untuk menandai string dalam JavaScript. 
// Namun, sebaiknya konsisten dalam menggunakan salah satu jenis tanda kutip untuk menjaga konsistensi dalam kode.