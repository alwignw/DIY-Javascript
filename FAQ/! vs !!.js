// Dalam JavaScript, tanda seru (!) digunakan sebagai operator negasi. 
//Ini berarti bahwa ketika ditempatkan sebelum sebuah nilai atau ekspresi, 
//tanda seru akan membalikkan nilai kebalikan dari nilai boolean dari ekspresi tersebut. 
//Sebagai contoh, !true akan menghasilkan nilai false, sedangkan !false akan menghasilkan nilai true.

//Sementara itu, dua tanda seru (!!) digunakan untuk mengubah nilai atau ekspresi menjadi boolean. 
//Cara kerjanya adalah dengan mengubah nilai atau ekspresi ke boolean dan mengembalikan nilai boolean yang dihasilkan. 
//Jika nilai atau ekspresi awalnya sudah boolean, dua tanda seru tidak memiliki efek apa pun.
//Sebagai contoh, !!true akan menghasilkan nilai true, sedangkan !!undefined akan menghasilkan nilai false. 
//Dalam beberapa kasus, ini dapat digunakan untuk memastikan bahwa nilai yang dihasilkan selalu berupa boolean.


console.log(undefined)
console.log(!undefined)
console.log(!!undefined)

console.log(null)
console.log(!null)
console.log(!!null)

console.log("")
console.log(!"")
console.log(!!"")



